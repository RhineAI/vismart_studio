                            Dalam kehidupan sehari-hari pasti kamu sudah tidak asing mengenai istilah Branding, Selling dan Marketing. Namun ternyata banyak yang belum bisa membedakan tiga hal tersebut sehingga banyak penggunanya tertukar.

                            Sebagai orang-orang yang bergelut di dunia bisnis ataupun pemasaran, kamu dituntut untuk bisa memajukan bisnis atau perusahaan yang kamu jalankan. Melakukan aktifitas marketing, selling dan branding adalah salah satu cara yang bisa kamu lakukan. Sepintas ketiganya terlihat sama dan dapat dilakukan bersamaan. Tapi ternyata ketiganya memiliki beberapa perbedaan yang membuat kamu harus fokus mejalankan satu aktivitas dalam satu waktu untuk mendapatkan hasil yang maksimal. 
                            
                            Seperti beberapa hal dikutip dari yang telah disampaikan Subiakto Priosoedarsono yang merupakan seorang ahli branding dalam salah satu seminarnya. Berikut adalah hal yang membedakan Branding, Selling, dan Marketing.
                            
                            1. Tujuannya
                            Branding : Dalam Branding bertujuan untuk memberikan jati diri konsumen kepada brand kita.
                            
                            Selling : Tujuan dari aktivitas Selling adalah omzet atau seberapa banyak unit yang telah terjual, sehingga konten dan hal- hal  yang dilakukan pada saat itu harus membuat konsumen membeli.
                            
                            Marketing : Aktivitas ini memiliki tujuan akhir yakni meningkatkan Market Share  alias pangsa pasar perusahaan (presentase penjualan jika dibandingkan dengan penjualan pasar), jika hari ini perusahaan hanya memiliki 10% pangsa pasar maka kamu perlu meningkatkan lagi aktivitas Marketing agar pangsa pasar perusahaan meningkat.
                            
                            2. Manfaat
                            Branding : Sifat nya mengaktivasi ikatan emosi. bentuknya berupa stimulan yang tujuannya memperkuat presepsi produk kamu di benak konsumen.
                            
                            Selling : Menciptakan komunikasi dengan dengan pembeli, dan juga seringkali yag kita tawarkan berupa hal fisik seperti kualitas produk, fungsi, dan sebagainya.
                            
                            Marketing : Manfaat yang ditawarkan dalam aktivitas Marketing ialah manfaat yang berbentuk fisik yakni produk perusahaan itu sendiri.
                            
                            3. Hal yang diperoleh konsumen
                            Dalam aktivitas marketing dan selling kita mempertanyakan tentang "apa yang konsumen peroleh" tentu saja disini kita membahas mengenai barang atau produk yang dijual. Sementara dalam Branding kita mempertanyakan tentang "konsumen menjadi apa"
                            contoh sederhana nya adalah sepatu Air Jordan . Dalam aktivitas Selling dan Marketing kita dapat membicarakan mengenai kualitas bahan, desain sepatu dan ketahanan sepatu tersebut.
                            
                            Namun didalam Branding kita akan menyebutkan bahwa Sepatu Air Jordan adalah lambang kesuksesan . Sehingga banyak orang sukses atau orang yang ingin di sebut sebagai orang sukses membeli sepatu tersebut. itu lah hal yang dimaksud dari "konsumen menjadi apa".
                            
                            4. Tujuan
                            Tujuan yang ingin dicapai oleh aktivitas Marketing adalah permintaan sehingga cara yang dilakukan dalam aktivitas ini adalah merangsang kebutuhan konsumen terhadap produk yang kamu tawarkan.
                            
                            Sementara tujuan dari Selling adalah ingin sampai pada tahap clossing atau penutup penjualan.
                            
                            Lain lagi dengan branding tujuan yang dicapai aktivitas ini adalah loyalitas konsumen. Pak Subiakto mengatakan bawa jika Selling dan Marketing menciptakan pembeli maka Branding menciptakan pelanggan.
                            
                            5. Waktu
                            Dari segi waktu, aktivitas Marketing memerlukan waktu jangka panjang. Dimulai dari mencari kebutuhan, mengedukasi pasar sehingga akhirnya bisa mencapai tujuan peningkatan pangsa pasar.
                            
                            Sementara aktivitas Selling bisa dilakukan dengan aktivitas jangka pendek dimulai dari hitungan jam hingga biasanya maksimal selama sebulan.
                            
                            Sedangkan Branding membutuhkan jangka waktu yang tidak sebentar. Branding dibuat melalui pengalaman-pengalaman yang dialami sehingga membekas di pikiran konsumen.
                            
                            Begitulah perbedaan Marketing, Selling dan Branding hingga contoh bagaimana menggunakan media untuk mencapai ketiganya. Semoga Bermanfaat.
